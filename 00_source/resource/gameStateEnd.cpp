//************************************************************
//	子クラス [CGameStateEnd] のメンバ関数
//************************************************************
//============================================================
//	コンストラクタ
//============================================================
CGameStateEnd::CGameStateEnd() :
	m_fCurTime	(0.0f)	// 経過時間
{

}

//============================================================
//	デストラクタ
//============================================================
CGameStateEnd::~CGameStateEnd()
{

}

//============================================================
//	初期化処理
//============================================================
HRESULT CGameStateEnd::Init()
{
	// メンバ変数を初期化
	m_fCurTime = 0.0f;	// 経過時間

	return S_OK;
}

//============================================================
//	終了処理
//============================================================
void CGameStateEnd::Uninit()
{
	// 自身の破棄
	delete this;
}

//============================================================
//	更新処理
//============================================================
void CGameStateEnd::Update(const float fDeltaTime)
{
	// 操作不可能状態にする
	m_pContext->SetEnableControlOK(false);

	// 経過時間を加算
	m_fCurTime += fDeltaTime;
	if (m_fCurTime >= 3.0f)
	{
		// リザルト画面に遷移する
		GET_MANAGER->SetLoadScene(CScene::MODE_RESULT);
	}
}
