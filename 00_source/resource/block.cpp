//============================================================
//	生成処理
//============================================================
CBlock* CBlock::Create
(
	const VECTOR3& rPos,	// 位置
	const bool bRight		// 右側
)
{
	// プレイヤーの生成
	CBlock* pBlock = new CBlock;
	if (pBlock == nullptr)
	{ // 生成に失敗した場合

		return nullptr;
	}
	else
	{ // 生成に成功した場合

		// プレイヤーの初期化
		if (FAILED(pBlock->Init()))
		{ // 初期化に失敗した場合

			// プレイヤーの破棄
			SAFE_DELETE(pBlock);
			return nullptr;
		}

		// 位置を設定
		pBlock->SetVec3Position(rPos);

		// 右側情報を設定
		pBlock->m_bRight = bRight;

		// 確保したアドレスを返す
		return pBlock;
	}
}

//============================================================
//	リスト取得処理
//============================================================
CListManager<CBlock>* CBlock::GetList()
{
	// オブジェクトリストを返す
	return m_pList;
}

//============================================================
//	ヒット処理
//============================================================
bool CBlock::Hit()
{
	// 死んでる場合抜ける
	if (IsDeath()) { return false; }

	return true;
}
